import { supabase } from '@/integrations/supabase/client';
import { EMAIL_CONFIG } from '@/config';

export interface EmailOptions {
  to: string | string[];
  subject: string;
  html: string;
  type?: string;
  data?: any;
}

export interface EmailResponse {
  success: boolean;
  data?: any;
  error?: string;
  message?: string;
  provider: string;
  from?: string;
  reply_to?: string;
}

/**
 * Send email using Resend API via Supabase Edge Function
 */
export const sendEmail = async (options: EmailOptions): Promise<EmailResponse> => {
  try {
    console.log('üìß Sending email via Resend API...');
    console.log('üìß To:', Array.isArray(options.to) ? options.to.join(', ') : options.to);
    console.log('üìß Subject:', options.subject);

    const { data, error } = await supabase.functions.invoke('send-notification-email', {
      body: {
        to: options.to,
        subject: options.subject,
        html: options.html,
        type: options.type,
        data: options.data
      }
    });

    if (error) {
      console.error('‚ùå Supabase function error:', error);
      throw error;
    }

    if (!data.success) {
      console.error('‚ùå Email send failed:', data.error);
      throw new Error(data.error || 'Email send failed');
    }

    console.log('‚úÖ Email sent successfully via Resend API');
    return {
      success: true,
      data: data.data,
      message: data.message,
      provider: 'resend',
      from: data.from,
      reply_to: data.reply_to
    };

  } catch (error: any) {
    console.error('‚ùå Email service error:', error);
    return {
      success: false,
      error: error.message || 'Unknown email error',
      provider: 'resend'
    };
  }
};

